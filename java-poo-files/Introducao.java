public class Introducao {

    public static void main (String[] args) {
        // output e concatenação
        System.out.println("Ola mundo!");
        System.out.println("Voce digitou: "+"service download");
        System.out.println("----------------------");

        // convenção de declaração de variaveis
        int valorMultiplicando = 18;
        int valorMultiplicador = 8;
        int retornoDaMultiplicacao = valorMultiplicando * valorMultiplicador;
        System.out.println(valorMultiplicando+" x "+valorMultiplicador+" = "+retornoDaMultiplicacao);
        System.out.println("----------------------");

        // usando variaveis e atribuindo valores
        int idade = 19;
        String nome = "Joao Victor";
        String nomeDoMeuCachorro = "Nico";
        System.out.println("Nome: "+nome);
        System.out.println("Idade: "+idade);
        System.out.println("Nome do cachorro: "+nomeDoMeuCachorro);
    }
}
